# 2000 Hz => 2000 esantioane pe secunda
print(1 / 2000, "secunde intre esantioane")

total_bits = 2000 * 60 * 60 * 4
print(total_bits, "biti pentru 1 ora de inregistrare")
print(total_bits / 8, "bytes pentru 1 ora de inregistrare")
print(total_bits / 8 / 1000, "Kb pentru 1 ora de inregistrare")
print(total_bits / 8 / 1000 / 1000, "Mb pentru 1 ora de inregistrare")


